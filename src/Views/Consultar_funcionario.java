    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Enfermero persona = new Enfermero();
        persona.setIdentificacion(TxtIdentificacion.getText());
        persona.setNombre(TxtNombre.getText()); 
        persona.setApellido(Txtapellido.getText());
        persona.setTelefono(TxtTelefono.getText());
        persona.setDireccion(TxtDireccion.getText());
        int id = CmbRol.getItemAt(CmbRol.getSelectedIndex()).getValue();
        String consulta = "update funcionario set identificacion = '" + TxtIdentificacion.getText() + "'," + "nombre = '" + TxtNombre.getText()+"',"+" apellido = '"+ Txtapellido.getText() +"'," +  " telefono ='" + TxtTelefono.getText() + "',"+ "direccion = '"+ TxtDireccion.getText()  + "',rol_funcionario = '" + id + "'";
        if (persona.updateFactory(consulta, "identificacion", TxtBuscar.getText())) {
            JOptionPane.showMessageDialog(null, "Se Actualizo");
            cargar_tabla();
        }else{
            JOptionPane.showMessageDialog(null, "No se Actualizo");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

