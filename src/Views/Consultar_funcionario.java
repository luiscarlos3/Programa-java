    private void load(){
        try {
            ResultSet combobox = Query.ComboBox("*", "rol");
        while(combobox.next()){
            CmbRol.addItem(new ParCombo(Integer.parseInt(combobox.getString(1)),combobox.getString(2)));
            
        }
            
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
     private void filtrar(String text){
        String [] Columnas = {"identificacion", "nombre", "apellido", "telefono", "direccion", "rol"};
        Query.buscar(jTable1, text, Columnas, sqlquery);
        
    }
    
     private void cargar_tabla(){
          
        try {
            
            DefaultTableModel modelo = new DefaultTableModel();
            jTable1.setModel(modelo);
            ResultSet load = Query.read(sqlquery);
            ResultSetMetaData rmsd = load.getMetaData();
            int cantidad = rmsd.getColumnCount();
            String [] Columnas = {"identificacion", "nombre", "apellido", "telefono", "direccion", "rol" };
            for (int i = 0; i < Columnas.length; i++) {
                modelo.addColumn(Columnas[i]);               
            }
            while(load.next())
            {
                 Object[] filas = new Object[cantidad];
                 for (int i = 0; i < cantidad; i++) {
                     filas[i] = load.getObject(i + 1);
                 }
                 modelo.addRow(filas);
                
            }
            
        } catch (Exception e) {
        }
        
    }

