    private void BtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnActualizarActionPerformed
        // TODO add your handling code here:
         Paciente persona = new Paciente();
        persona.setIdentificacion(TxtIdentificacion.getText());
        persona.setNombre(TxtNombre.getText());
        persona.setApellido(TxtApellido.getText());
        persona.setEdad(Integer.parseInt(TxtEdad.getText()));
        persona.setDireccion(TxtDireccion.getText());
        persona.setTelefono(TxtTelefono.getText());
        String consulta = "update paciente set identificacion_paciente = '" + TxtIdentificacion.getText()+ "'," + "nombre_paciente = '" + TxtNombre.getText() + "'," + "apellido_paciente = '"+ TxtApellido.getText()+"',"+ "edad = '"+ Integer.parseInt(TxtEdad.getText()) +"'," +" telefono ='" + TxtTelefono.getText() + "'," + " direccion = '" + TxtDireccion.getText()+"'";
        if (persona.updateFactory(consulta, "identificacion_paciente", TxtBuscar.getText())) {
            JOptionPane.showMessageDialog(null, "Se Actualizo");
            this.cargar_tabla();
        }else{
            JOptionPane.showMessageDialog(null, "No se Actualizo");
        }

    }//GEN-LAST:event_BtnActualizarActionPerformed

